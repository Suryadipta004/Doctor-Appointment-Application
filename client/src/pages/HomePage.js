import React, { useEffect, useState } from "react";
import axios from "axios";
import Layout from "./../components/Layout";
import { Row } from "antd";
import DoctorList from "../components/DoctorList";
const HomePage = () => {
  const [doctors, setDoctors] = useState([]);
  // login user data
  const getUserData = async () => {
    try {
      const res = await axios.get(
        "/api/v1/user/getAllDoctors",

        {
          headers: {
            Authorization: "Bearer " + localStorage.getItem("token"),
          },
        }
      );
      if (res.data.success) {
        setDoctors(res.data.data);
      }
    } catch (error) {
      console.log(error);
    }
  };

  useEffect(() => {
    getUserData();
  }, []);
  return (
    <Layout>
      <h1 className="text-center">
        {/* <span style={{ fontSize: '2rem', color: '#333' }}>Welcome to</span><br/>
        <span style={{ fontSize: '3rem', fontWeight: 'bold', fontFamily: 'Arial, sans-serif', color: '#007bff' }}>Our Home Page</span><br/>
        <span style={{ fontSize: '1.2rem', fontStyle: 'italic', color: '#666' }}>Discover what's new</span> */}
      </h1>
      <Row>
        {doctors && doctors.map((doctor) => <DoctorList doctor={doctor} />)}
      </Row>
    </Layout>
  );
};

export default HomePage;
